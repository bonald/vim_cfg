#define TPOAM_MAX_LOG_OFFSET tpoam_log_msg_len/LOG_MSG_SIZE

#define TPOAM_LOG_USER(eSeverity, code, ...)\
do {\
    if (code < TPOAM_MAX_LOG_OFFSET)\
    {\
         log_sys(M_MOD_TPOAM, eSeverity, tpoam_log_message[code].str, ##__VA_ARGS__);\
    }\
}while(0)


